package Library;
import java.util.*;
 class Sorting {
     public static void main(String[] args) {
         int arr[]={6,7,4,9,3};
         //bubble sort
         for(int i=0;i<arr.length-1;i++){
             for
             (int j=0;j< arr.length-1-i;j++)
             {
                 if(arr[i]>arr[i+1]){
                     //swap
                     int temp=arr[i];
                     arr[i]=arr[i+1];
                     
                 }
             }
         }
     }
}
